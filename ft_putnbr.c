#include "libft.h"

void	ft_putnbr(long int n)
{
	long int n2;

	n2 = n < 0 ? -n : n;
	if (n < 0)
		ft_putchar('-');
	if (n2 < 10)
		ft_putchar(n2 + '0');
	else
	{
		ft_putnbr(n2 / 10);
		ft_putchar((n2 % 10) + '0');
	}
}
